a = [4,3,2,1]
b = []
c = []
name_dict = {
    "A": a,
    "B": b,
    "C": c
}

def move(f,t):
    print(f"Move disc from {f} to {t}.")
    temp = name_dict[f].pop()
    name_dict[t].append(temp)
    line_1 = ''
    line_2 = ''
    line_3 = ''
    line_4 = ''

    if len(a) > 3:
        line_1 += (" "*(7-a[3]//2) +'#'*a[3] + " "*(7-a[3]//2))
    else:
        line_1 += (" "*(5) +'||' + " "*(5))
    if len(b) > 3:
        line_1 += (" "*(7-b[3]) +'#'*b[3])
    else:
        line_1 += (" "*(5) +'||' + " "*(5))
    if len(c) > 3:
        line_1 += (" "*(7-c[3]//2) +'#'*c[3] + " "*(7-c[3]//2))
    else:
        line_1 += (" "*(5) +'||' + " "*(5))
    if len(a) > 2:
        line_2 += (" "*(7-a[2]//2) +'#'*a[2] + " "*(7-a[2]//2))
    else:
        line_2 += (" "*(5) +'||' + " "*(5))
    if len(b) > 2:
        line_2 += (" "*(7-b[2]//2) +'#'*b[2] + " "*(7-b[2]//2))
    else:
        line_2 += (" "*(5) +'||' + " "*(5))
    if len(c) > 2:
        line_2 += (" "*(7-c[2]//2) +'#'*c[2] + " "*(7-c[2]//2))
    else:
        line_2 += (" "*(5) +'||' + " "*(5))
    if len(a) > 1:
        line_3 += (" "*(7-a[1]//2) +'#'*a[1] + " "*(7-a[1]//2))
    else:
        line_3 += (" "*(5) +'||' + " "*(5))
    if len(b) > 1:
        line_3 += (" "*(7-b[1]//2) +'#'*b[1] + " "*(7-b[1]//2))
    else:
        line_3 += (" "*(5) +'||' + " "*(5))
    if len(c) > 1:
        line_3 += (" "*(7-c[1]//2) +'#'*c[1] + " "*(7-c[1]//2))
    else:
        line_3 += (" "*(5) +'||' + " "*(5))
    if len(a) > 0:
        # line_4 += (" "*(7-a[0]) +'#'*a[0])
        line_4 += (" "*(7-a[0]//2) +'#'*a[0] + " "*(7-a[0]//2))
    else:
        line_4 += (" "*(5) +'||' + " "*(5))
    if len(b) > 0:
        line_4 += (" "*(7-b[0]//2) +'#'*b[0] + " "*(7-b[0]//2))
    else:
        line_4 += (" "*(5) +'||' + " "*(5))
    if len(c) > 0:
        line_4 += (" "*(7-c[0]//2) +'#'*c[0] + " "*(7-c[0]//2))
    else:
        line_4 += (" "*(5) +'||' + " "*(5))

    print(f'''
    {line_1}
    {line_2}
    {line_3}
    {line_4}
    ||||||||||||||||||||||||||||||||||||||||||||||||||||||||
    ''')


def hanoi(n, f, h, t):
    if n == 1:
        move(f,t)
    else:
        hanoi(n-1,f,t,h)
        move(f,t)
        hanoi(n-1,h,f,t)

hanoi(4,"A","B","C")
